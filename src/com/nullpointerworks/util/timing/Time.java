package com.nullpointerworks.util.timing;

public class Time 
{
	public static final long	MILLI 	= 1_000;			// 10^3
	public static final long	MICRO 	= 1_000_000;		// 10^6
	public static final long	NANO 	= 1_000_000_000;	// 10^9
	
	public static final double	inv_MILLI 	= 1.0 / MILLI;	// 10^-3
	public static final double	inv_MICRO 	= 1.0 / MICRO;	// 10^-6
	public static final double	inv_NANO 	= 1.0 / NANO;	// 10^-9
}
